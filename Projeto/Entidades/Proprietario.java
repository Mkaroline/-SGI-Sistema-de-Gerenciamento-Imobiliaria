package Projeto.Entidades;

public class Proprietario {
    
    private String nome;
    private int numero;
    private String cidade;
    private String cep;

    
    public Proprietario(String nome, int numero, String cidade, String cep) {
        this.nome = nome;
        this.numero = numero;
        this.cidade = cidade;
        this.cep = cep;
    }

    
}
